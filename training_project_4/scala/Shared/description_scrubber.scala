  def description_scrubber(desc: String): String = {
    val desc_not_empty = desc + " "
    var wordArray = desc_not_empty.replaceAll("\\W", " ").split(" ")
    wordArray = wordArray
      .map(_.toLowerCase())
      .map(_.trim)
      .filter(_.length > 2)
      .distinct

    val result = new StringBuffer()

    for (word <- wordArray) {
      if (passes_filter(word)) {
        if (result.length == 0)
          result.append(word)
        else
          result.append("  " + word)
      }
    }

    result.toString
  }

  def passes_filter(str: String): Boolean = {
    var bool = true

    if (FILTER_WORDS_LIST_1.contains(str))
      bool = false
    if (FILTER_WORDS_LIST_2.contains(str))
      bool = false

    bool
  }
